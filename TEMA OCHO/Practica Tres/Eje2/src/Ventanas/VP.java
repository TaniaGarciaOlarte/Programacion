    private void bInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInscribirActionPerformed
        try
        {
            if (jtNombre.getText().isEmpty() || jtApellidos.getText().isEmpty() || jtTelefono.getText().isEmpty() || jtCorreo.getText().isEmpty())
              throw new CampoObligatorio();  
                        
            validarnombre(jtNombre);
            defecto(jtNombre);
            validarapellidos();
            if (Eje2.comprobarsocio() != 0)
            {
                if (Eje2.comprobarsiexiste(jtNombre.getText().toUpperCase(),jtApellidos.getText().toUpperCase()))
                throw new Existente();
            }
            
            defecto(jtNombre);
            defecto(jtApellidos);
                    
            validartelefono();
            defecto(jtTelefono);
            validarcorreo();
            defecto(jtCorreo);
            ErrCampo.setText("");
            
            if (jrFamiliar.isSelected())
            {
                try
                {
                    if (Eje2.comprobarhijo() == 0)
                        throw new HijoError();
                    else
                    {    
                        ErrHijo.setText("");
                        Eje2.añadirsociofam(jtNombre.getText().toUpperCase(),jtApellidos.getText().toUpperCase(),jtTelefono.getText(),jtCorreo.getText()); 
                        showMessageDialog(this,"ALTA DE SOCIO CORRECTA");
                        limp();
                        Eje2.crearlista();
                    }           
                }
                catch (HijoError he)
                {
                    ErrHijo.setText("DEBES AÑADIR UN HIJO MINIMO");
                    jtNombreHij.setEnabled(true);
                    jtNombreHij.requestFocus();
                    jDia.setEnabled(true);
                    jMes.setEnabled(true);
                    jAñoNac.setEnabled(true);
                    bBorrar.setEnabled(true);
                    bAñadir.setEnabled(true);
                }
            }     
            else
            {
                Eje2.añadirsocioindi(jtNombre.getText().toUpperCase(),jtApellidos.getText().toUpperCase(),jtTelefono.getText(),jtCorreo.getText());
                showMessageDialog(this,"ALTA CORRECTA DEL SOCIO"); 
                limp();
            }
        }
        catch (CampoObligatorio npe)
        {
            showMessageDialog(this,"QUEDAN CAMPOS OBLIGATORIOS POR COMPLETAR");
        }
        catch(DatoNoValido dnv)
        {
            ErrCampo.setText(dnv.getCampo());
            dnv.getCuadro().setBackground(Color.RED);
            dnv.getCuadro().requestFocus();
            dnv.getCuadro().setEnabled(true);
        }
        catch (Existente e)
        {
            jtNombre.setBackground(Color.RED);
            jtApellidos.setBackground(Color.RED);
            jtNombre.setEnabled(true);
            jtApellidos.setEnabled(true);
            jtNombre.requestFocus();
            ErrCampo.setText("ESTA PERSONA YA ESTA DADA DE ALTA");
        }
    }//GEN-LAST:event_bInscribirActionPerformed

