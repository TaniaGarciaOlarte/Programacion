    public void validarnombre(javax.swing.JTextField jt) throws DatoNoValido
    {   
        if (!Eje2.expreregular(jt.getText().toUpperCase(),"^[A-Z]+$"))
            throw new DatoNoValido("NOMBRE INCORRECTO (SOLO LETRAS)",jt);
    }
    
    public void validarapellidos() throws DatoNoValido
    {   
        if (!Eje2.expreregular(jtApellidos.getText().toUpperCase(),"^[A-Z]+\\s[A-Z]+$"))
            throw new DatoNoValido("APELLIDOS INCORRECTO (LOS DOS APELLIDOS)",jtApellidos);     
    }
    
    public void validartelefono() throws DatoNoValido
    {
        if (!Eje2.expreregular(jtTelefono.getText(),"^[6|7][\\d]{8}$"))
            throw new DatoNoValido("TELEFONO INCORRECTO",jtTelefono);   
    }
    
    public void validarcorreo() throws DatoNoValido
    {
        if (!Eje2.expreregular(jtCorreo.getText(),"^[-a-zA-z0-9_]+(.[-a-zA-Z0-9_]+)*@(gmail|hotmail).(com|es|org)$"))
            throw new DatoNoValido("CORREO ELECTRONICO INCORRECTO",jtCorreo);
        
        if (Eje2.comprobarcorreo(jtCorreo.getText()))
            throw new DatoNoValido("CORREO YA EXISTENTE",jtCorreo);
    }
    
