    private boolean datoscorrectos()
    {
        try
        {
            if (tfNif.getText().isEmpty())
                throw new CampoObligatorio();   
            
            jCampoR.setVisible(false); 
            
            if (!Eje1.validarformato(tfNif.getText().toUpperCase(),"^[\\d]{8}[A-Z]"))
                throw new DatoNoValido("Comprueba que has introducido los datos correctamente");
            
            if (Eje1.validarDni(tfNif.getText()))
            {
                if (Eje1.validarclave(clave))
                    return true;
                else
                    throw new DatoNoValido("El NIF o la clave de acceso son incorrectos");
            }
            else
                throw new DatoNoValido("El NIF o la clave de acceso son incorrectos");
        }
        catch (DatoNoValido dnv)
        {
            tfErr.setText(dnv.getCampo());
            jPanelErr.setVisible(true);
            jPanel1.setVisible(false);
            tfNif.setText("");
            tfClave.setText("");
            tfNif.requestFocus();
            clave = "";
            
            return false;
        }
        catch (CampoObligatorio co)
        {
            jCampoR.setVisible(true);
            tfNif.requestFocus();
            clave = "";
            return false;
        }
    }
    
