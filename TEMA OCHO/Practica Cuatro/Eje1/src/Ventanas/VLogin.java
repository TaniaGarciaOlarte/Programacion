    public void generarnumeros()
    {
        Integer[] numeroale = new Integer[10];
        Arrays.fill(numeroale,-1);
        
        int pos = 0;
        do
        {
            Integer numeroal = (int) (Math.random()*10);
            
            boolean repetido = false;
            for (int x = 0; x < pos && !repetido;x++)
                if (numeroale[x] == numeroal)
                    repetido = true;
            if (!repetido)
            {
                numeroale[pos] = numeroal;
                pos++;
            }
        }
        while(pos != 10);
        
        JButton[] listabotones = {b0,b1,b2,b3,b4,b5,b6,b7,b8,b9};
        for (int x = 0; x < numeroale.length;x++)
            listabotones[x].setText(numeroale[x].toString());
    }
    
