    private void cuentaSeleccionada(int c)
    {

        if (operacion.equals("Consulta"))
        {
            //Mostrar movimientos de la cuenta c
                mostrarMovimiento(c);                    
                pMov.setVisible(true);
                lSaldo.setText("Saldo = " + nf.format(Eje1.getSaldo()));
                lSaldo.setVisible(true);
        }
        else
        {
            pTran.setVisible(true);
        }    
    }

    
    public void mostrarMovimiento(int c)
    {
        desplazamiento = 20;
        listaetiquetas.clear();
        pMov.removeAll();
        int nro = Eje1.getnmov();
        
        if (nro <=5)
        {
            for (int x=0;x<nro;x++)
            {
                creareti(x);
            }
        }
        else
        {
            for (int x = 0,cuenta = nro -5; x < 5; x++,cuenta++)
            {
                creareti(cuenta);
            }
        }
    }
    
