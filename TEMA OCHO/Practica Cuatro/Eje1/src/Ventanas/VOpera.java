    private boolean datosCorrectos()
    {
        try
        {
            if (jcTipo.getSelectedIndex() == 1)
            {
                if (!Eje1.validarformato(tfCuenta.getText().toUpperCase(), "^[\\d]{8}[-][A-Z]{2}$"))
                    throw new DatoNoValido();
            }
            else
            {
                if (!Eje1.validarformato(tfCuenta.getText(), "^[\\d]{20}$"))
                    throw new DatoNoValido();
                
                if (tfCuenta.getText().equals(Eje1.numcuenta(z)))
                    throw new CampoIncorrecto();
            }
            double importe = Double.parseDouble(tfImporte.getText());
            if(importe < 0 || (Eje1.getSaldo() - importe) < 0)
                throw new DatoNoValido();
            return true;
        }
        catch (CampoIncorrecto ci)
        {
            showMessageDialog(this,"NO PUEDES REALIZAR UNA TRANSFERENCIA A TU MISMO NUMERO DE CUENTA");
            return false;
        }
        catch (DatoNoValido dnv)
        {
            showMessageDialog(this,"DATOS NO VALIDOS O NO HAY SALDO");
            return false;
        }
        catch (NumberFormatException e)
        {
            showMessageDialog(this,"DATOS NO VALIDOS");
            return false;
        }
    }
    
