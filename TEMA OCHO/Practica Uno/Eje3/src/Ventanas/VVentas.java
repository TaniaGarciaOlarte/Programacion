    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
       try{
        tfErr.setVisible(false);
        boolean error;
        
        prod = tfProducto.getText().toUpperCase();
        boolean pre = Eje3.comprobarproduct(prod,"^[A-Z]+$");
        
        if (!pre)
        {
          throw new DatoNoValido("Producto");   
        }
        else
        {
            tfPro.setVisible(false);
            boolean pro = producto();
            int unida = Integer.parseInt(tfUnidades.getText());
            tfProducto.setText("");
            tfUnidades.setText("");
            tfImporte.setText("");
       
            if (pro)
            {
                uni = Eje3.comprobaruni(prod,unida);
                if (uni < 0)
                    {
                        tTexto.setText("NO HAY TANTAS UNIDADES");
                        error = false;
                    }
                else
                    error = true;
           
            if (error)
            {
                showMessageDialog(this,"Venta Correcta");
                Eje3.volver(this);
            }
            
        }
        }
        }
        catch (DatoNoValido dnv)
        {
            tfPro.setVisible(true);
            tfPro.setText(dnv.getCampo());
        }
        catch (NumberFormatException nfe)
        {
           tfErr.setVisible(true);
           tfErr.setText("SOLO NUMEROS");
        }
    }//GEN-LAST:event_bAceptarActionPerformed

