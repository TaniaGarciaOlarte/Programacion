    private boolean producto()
    {
       tTexto.setText("");
        
       String producto = tfProducto.getText().toUpperCase();
       
       if (!Eje3.comprobarpro(producto))
       {
           tTexto.setText("PRODUCTO INEXISTENTE");
           return false;
       }
       
       return true;
    }
    
