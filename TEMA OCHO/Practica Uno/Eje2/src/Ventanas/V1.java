    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try
        {
            defecto();
            
            boolean n = validardato(tfNombre.getText(),"NOMBRE","^[A-Za-z]+([ ][A-Za-z]+)?$");
            boolean a = validardato(tfApellidos.getText(),"APELLIDOS","^[A-Za-z]+([ ][A-Za-z]+)$");
            boolean d = validardni();
        
            if (n == true && a == true && d == true)
            {
                String nombre = tfNombre.getText();
                String apellidos = tfApellidos.getText();
                String dni = tfDni.getText();
               
                Eje2.altapersona(nombre, apellidos, dni);
                showMessageDialog(null,"Alta correcta");
                limpiar();
            }
        }
        catch (Exception e)
        {
            showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_bAceptarActionPerformed

