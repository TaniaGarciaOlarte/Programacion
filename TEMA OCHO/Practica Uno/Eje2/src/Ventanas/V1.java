    public void visualizartext(DatoNoValido dnv)
    {
        if (dnv.getCampo().equalsIgnoreCase("NOMBRE"))
            {
                jNombre.setText("NOMBRE no valido");
                jNombre.setForeground(Color.RED);
                jNombre.setVisible(true);
                tfNombre.setBorder(javax.swing.BorderFactory.createLineBorder(Color.RED));
            }
        else
            {
                jApellido.setText("APELLIDO no valido");
                jApellido.setForeground(Color.RED);
                jApellido.setVisible(true);
                tfApellidos.setBorder(javax.swing.BorderFactory.createLineBorder(Color.RED));
            }    
    }
    
    public void limpiar()
    {
        tfNombre.setText("");
        tfApellidos.setText("");
        tfDni.setText("");
    }
    
    public boolean validardato(String cuadro,String dato,String expre) throws Exception
    {   
        try{
            Pattern pt = Pattern.compile(expre);
            Matcher m = pt.matcher(cuadro);
            
            if (!m.matches())
                throw new DatoNoValido(dato);
            
            return true;
        }
        catch (DatoNoValido dnv)
        {
            visualizartext(dnv);
            return false;
        }
    }
    
    public boolean validardni() throws Exception
    {
        String dni = null;
        try{
            dni = tfDni.getText();

            Pattern pt = Pattern.compile("^[0-9]{8}[A-Z]$");
            Matcher m = pt.matcher(dni);

            if (!m.matches())
             throw new DatoNoValido("DNI");
            
            if (Eje2.validardni(dni))
                throw new Dninovalido();
            
            return true;
        }
        catch (DatoNoValido dnv)
        {
            jDni.setText("DNI no valido");
            jDni.setForeground(Color.RED);
            jDni.setVisible(true);
            tfDni.setBorder(javax.swing.BorderFactory.createLineBorder(Color.RED));
            
            return false;
        }
        catch (Dninovalido dnva)
        {
            dninovalido.setText("Ya hay una persona dada de alta con ese dni");
            dninovalido.setVisible(true);
            tfDni.setBorder(javax.swing.BorderFactory.createLineBorder(Color.RED));
            return false;
        }
    }
    
