    private void tfNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfNombreActionPerformed
        try
        {
            if (tfNombre.getText().isEmpty())
                throw new Exception("CAMPO OBLIGATORIO");
            if (!validardato(tfNombre.getText().toUpperCase(),"^[A-Z]+$"))
                throw new Exception("NOMBRE INCORRECTO");
            if (Eje1.consultarpersona(tfNombre.getText().toUpperCase()))
                throw new Exception("EL NOMBRE YA ESTA EN LA BD");
            
            defecto(tfNombre,tfEdad);
        }
        catch (Exception e)
        {
            error(tfNombre,e);
        }    
    }//GEN-LAST:event_tfNombreActionPerformed

    private void tfEdadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfEdadActionPerformed
        try
        {
            if (tfEdad.getText().isEmpty())
                throw new Exception("CAMPO OBLIGATORIO");
            if(!validardato(tfEdad.getText(),"^[1-9][0-9]?$"))
                throw new Exception("EDAD INCORRECTA");
            
            defecto(tfEdad,tfProfesion);
        }
        catch (Exception e)
        {
            error(tfEdad,e);
        }
    }//GEN-LAST:event_tfEdadActionPerformed

    public void error(javax.swing.JTextField tf,Exception e)
    {
        jlErr.setText(e.getMessage());
        tf.setBackground(Color.red);
    }
    
    private void tfProfesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfProfesionActionPerformed
        try
        {
            if (tfProfesion.getText().isEmpty())
                throw new Exception("CAMPO OBLIGATORIO");
            if(!validardato(tfProfesion.getText().toUpperCase(),"^[A-Z]+( [A-Z]+)*$"))
                throw new Exception("PROFESION INCORRECTA");
            
            defecto(tfProfesion,tfTelefono);
        }
        catch (Exception e)
        {
            error(tfProfesion,e);
        }
    }//GEN-LAST:event_tfProfesionActionPerformed

    private void tfTelefonoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfTelefonoActionPerformed
       try
        {
            if (tfTelefono.getText().isEmpty())
                throw new Exception("CAMPO OBLIGATORIO");
            if(!validardato(tfTelefono.getText().toUpperCase(),"^[6|7][\\d]{8}$"))
                throw new Exception("TELEFONO INCORRECTO");
            
           jlErr.setText("");
           tfTelefono.setBackground(Color.WHITE);
           tfTelefono.setEnabled(false);
           bAceptar.setEnabled(true);
           bAceptar.requestFocus();
        }
        catch (Exception e)
        {
            error(tfTelefono,e);
        }
    }//GEN-LAST:event_tfTelefonoActionPerformed

