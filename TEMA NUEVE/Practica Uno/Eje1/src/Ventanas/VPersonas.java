    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        if (operacion.equals("Consultar"))
        {
            cont = 1;
            mostrardatos();
            bIzq.setEnabled(false);

            try 
            {
                tlista = Eje1.personasreg();
                if (tlista ==1)
                   bDer.setEnabled(false);

            } 
            catch (Exception e) {
                showMessageDialog(this,e.getMessage());
            }
        }
    }//GEN-LAST:event_formWindowActivated

    private void bDerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDerActionPerformed
        cont++;
        bIzq.setEnabled(true);
        if (cont == tlista)
            bDer.setEnabled(false);
        mostrardatos();
    }//GEN-LAST:event_bDerActionPerformed

    private void bIzqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bIzqActionPerformed
        cont--;
        bDer.setEnabled(true);
        if (cont == 1)
            bIzq.setEnabled(false);
        mostrardatos();
    }//GEN-LAST:event_bIzqActionPerformed

    public void mostrardatos()
    {
        tfNombre.setText(Eje1.mostrarnombre(cont-1));
        tfEdad.setText(Eje1.mostraredad(cont-1));
        tfProfesion.setText(Eje1.mostrarprofesion(cont-1));
        tfTelefono.setText(Eje1.mostrartelefono(cont-1));
    }
    
    public void defecto(javax.swing.JTextField tf, javax.swing.JTextField td)
    {
        jlErr.setText("");
        tf.setBackground(Color.WHITE);
        tf.setEnabled(false);
        td.setEditable(true);
        td.requestFocus();
    }
        
    private boolean validardato(String dato,String expre)
    {
        Pattern pt = Pattern.compile(expre);
        Matcher m = pt.matcher(dato);
        return m.matches();  
        /*
            if (!m.matches())
                return false;
            return true;  
        */
    }
        
